# Predictions
# Let's start by writing a routine similar to Tensorflow's model.predict(). 
# This will take a matrix  ð‘‹  with all  ð‘š  examples in the rows and make a prediction by running the model.
def my_predict(X, W1, b1, W2, b2):
    m = X.shape[0]
    p = np.zeros((m,1))
    for i in range(m):
        p[i,0] = my_sequential(X[i], W1, b1, W2, b2)
    return(p)

